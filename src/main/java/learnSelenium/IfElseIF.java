package learnSelenium;

public class IfElseIF {

	public static void main(String[] args) {
	 
		int D = 6;
		
		if (D > 6) {
			System.out.println("I am diabetic");
		}  else if (D < 6)  {
			System.out.println("I am no diabetic");
		}  else if (D > 7) {
			System.out.println("I have less control");
		}  else {
			System.out.println("I am a pre diabetic");
		}
		
		System.out.println("-------------------------------------------");

		
		
		
		
	}

}
